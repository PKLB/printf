#include "ft_printf.h"

int	ft_putnbr_f(int n)
{
	int	count;

	count = 0;
	if (n == -2147483648)
	{
		write (1, "-2147483648", 11);
		return (11);
	}
	if (n < 0)
	{
		n *= -1;
		write (1, "-", 1);
		count++;
	}
	if (n > 9)
		ft_putnbr_f(n / 10);
	ft_putchar_f(n % 10 + '0');
	count += ft_int_len(n);
	return (count);
}
